n=int(input("enter the value of numbers"))
print(f"natural numbers between 1 to {n} :")
for i in range(n+1):
    print(i)
# printing reverse of natural numbers
n1=int(input("enter the value of numbers"))
print(f"natural numbers between 1 to {n} in reverse :")
for i in range(n1,0,-1):
    print(i)
#printing cubes of numbers
